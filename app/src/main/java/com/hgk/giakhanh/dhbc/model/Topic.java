package com.hgk.giakhanh.dhbc.model;


public class Topic {
    int id;
    String Name;

    public Topic(int id, String name) {
        this.id = id;
        Name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
